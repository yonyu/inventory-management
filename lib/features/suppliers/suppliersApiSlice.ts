// Need to use the React-specific entry point to import `createApi`
import { createApi, fetchBaseQuery } from "@reduxjs/toolkit/query/react";

interface Supplier {
    _id?: string;
    name: string;
    email: string;
    phone: string;
    address: string;
    status: boolean;
}

interface SuppliersApiResponse {
    suppliers: Supplier[];
}

export interface SuppliersSliceState {
    suppliers: Supplier[];
    status: "idle" | "loading" | "failed" | "fulfilled";
}

const initialState: SuppliersSliceState = {
    suppliers: [],
    status: "idle",
};



// Create RTK Query API slice for supplier operations
// RTK Query provides automatic caching, background refetching, and optimistic updates
export const suppliersApiSlice = createApi({
    // Base query configuration - sets the base URL for all endpoints
    baseQuery: fetchBaseQuery({ baseUrl: "/api/user/supplier" }),
    // Unique key for this API slice in the Redux store
    reducerPath: "suppliersApi",
    // Tag types are used for caching and invalidation
    // When a mutation invalidates a tag, all queries with that tag will refetch
    tagTypes: ["Suppliers"],
    // Define API endpoints (queries for fetching data, mutations for modifying data)
    endpoints: (build) => ({
        // Supply generics for the return type (in this case `SuppliersApiResponse`)
        // and the expected query argument. If there is no argument, use `void`
        // for the argument type instead.
        getSuppliers: build.query<SuppliersApiResponse, void>({
            query: () => '',
            providesTags: ['Suppliers'],
        }),
        getSupplier: build.query<SuppliersApiResponse, string>({
            query: (name) => `?name=${name}`,
            providesTags: ['Suppliers'],
        }),
        addSupplier: build.mutation<Supplier, Omit<Supplier, 'status'>>({
            query: (newSupplier) => ({
                url: '',
                method: 'POST',
                body: newSupplier,
            }),
            invalidatesTags: [{ type: 'Suppliers' }],
        }),
        updateSupplier: build.mutation<Supplier, { _id: string; data: Partial<Supplier> }>({
            query: ({ _id, data }) => ({
                url: `/${_id}`,
                method: 'PUT',
                body: data,
            }),
            invalidatesTags: [{ type: 'Suppliers' }],
        }),
        deleteSupplier: build.mutation<{ deletingSupplier: Supplier }, string>({
            query: (id) => ({
                url: `/${id}`,
                method: 'DELETE',
            }),
            invalidatesTags: [{ type: 'Suppliers' }],
        }),
        getSupplierFilteredByCategoryId: build.query<SuppliersApiResponse, string>({
            query: (categoryId) => `?categoryId=${categoryId}`,
            providesTags: ['Suppliers'],
        }),
    }),
});

// Hooks are auto-generated by RTK-Query
// Same as `suppliersApiSlice.endpoints.getSuppliers.useQuery`
// Same as `suppliersApiSlice.endpoints.getSupplier.useQuery`
// Same as `suppliersApiSlice.endpoints.addSupplier.useMutation`
// Same as `suppliersApiSlice.endpoints.updateSupplier.useMutation`
// Same as `suppliersApiSlice.endpoints.deleteSupplier.useMutation`
export const { useGetSuppliersQuery, useGetSupplierQuery, useAddSupplierMutation, useUpdateSupplierMutation, useDeleteSupplierMutation, useGetSupplierFilteredByCategoryIdQuery } = suppliersApiSlice;
